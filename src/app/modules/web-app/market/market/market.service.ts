  /**
   * Get by id
   */
  getSymbolById(coinFullName: string): Observable<any> {
    return this._symbols.pipe(
      take(1),
      map((symbols) => {
        // Find
        const symbol = symbols.find(item => item.coinFullName === coinFullName) || null;

        // Update
        this._symbol.next(symbol);

        // Return
        return symbol;
      }),
      switchMap((symbol) => {
        if (!symbol) {
          const err = new Error('Could not found symbol with name of ' + coinFullName + '!');
          return throwError(() => err);
        }

        return this._symbol;
      })
    );
  }

